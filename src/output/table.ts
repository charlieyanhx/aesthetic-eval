/**
 * Human-readable table output formatter.
 * Matches the visual style of the original evaluate.mjs.
 */

import type { OverallResult } from "../scoring/aggregator.js";
import type { AuditResult } from "../audits/types.js";

/** Category display names and their config weight keys. */
const DISPLAY_NAMES: Record<string, string> = {
  "color-contrast": "Color & Contrast",
  "typography": "Typography",
  "spacing": "Spacing Consistency",
  "layout": "Layout & Structure",
  "imagery": "Imagery",
  "accessibility": "Accessibility",
  "performance": "Performance Indicators",
  "animation": "Animation & Interaction",
  "cross-browser": "Cross-Browser & Device Compat",
  "text-wrapping": "Text Wrapping & Line Breaking",
};

function statusIcon(score: number): string {
  if (score >= 80) return "\u2713"; // checkmark
  if (score >= 60) return "\u25B3"; // triangle
  return "\u2717"; // cross
}

export function formatTable(result: OverallResult, pagesAnalyzed: number, cssFiles: number): void {
  console.log("\n\u2554\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2557");
  console.log("\u2551          AESTHETIC QUALITY EVALUATION REPORT            \u2551");
  console.log("\u255A\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u2550\u255D\n");

  console.log(`  Pages analyzed: ${pagesAnalyzed}`);
  console.log(`  CSS files analyzed: ${cssFiles}\n`);

  console.log("\u250C\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u252C\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u252C\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2510");
  console.log("\u2502 Category                         \u2502 Score \u2502 Weight \u2502");
  console.log("\u251C\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u253C\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u253C\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2524");

  for (const cat of result.categories) {
    const name = (DISPLAY_NAMES[cat.id] || cat.name).padEnd(33);
    const score = String(cat.score).padStart(3);
    const weight = (cat.weight * 100).toFixed(0).padStart(2);
    const icon = statusIcon(cat.score);
    console.log(`\u2502 ${name}\u2502 ${score}/100\u2502  ${weight}%  \u2502 ${icon}`);
  }

  console.log("\u251C\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u253C\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u253C\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2524");
  console.log(`\u2502 OVERALL                          \u2502 ${String(result.score).padStart(3)}/100\u2502  100% \u2502`);
  console.log("\u2514\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2534\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2534\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2518\n");

  console.log(`  Overall Grade: ${result.grade}\n`);

  // Recommendations
  const allRecs = result.categories.filter((c) => c.recommendations.length > 0);
  if (allRecs.length > 0) {
    console.log("\u250C\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2510");
    console.log("\u2502                   RECOMMENDATIONS                      \u2502");
    console.log("\u2514\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2518\n");
    for (const cat of allRecs) {
      console.log(`  [${DISPLAY_NAMES[cat.id] || cat.name}] (score: ${cat.score}/100)`);
      for (const rec of cat.recommendations) {
        console.log(`    - ${rec}`);
      }
      console.log();
    }
  } else {
    console.log("  No critical recommendations - all categories scored 80+!\n");
  }
}
